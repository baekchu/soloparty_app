// ==================== 공휴일 맵 (평탄화) ====================
// 형식: { 'YYYY-MM-DD': '공휴일명' }
const _cache: Record<string, string> = {};

function _build(): void {
  const years = [2025, 2026, 2027, 2028];
  for (const year of years) {
    const ys = String(year);
    // 고정 공휴일
    for (const [mmdd, name] of Object.entries(FIXED_HOLIDAYS)) {
      _cache[`${ys}-${mmdd}`] = name;
    }
    // 연도별 공휴일 (고정보다 나중에 병합 → 겹치면 연도별 값 우선)
    for (const [mmdd, name] of Object.entries(YEARLY_HOLIDAYS[ys] || {})) {
      _cache[`${ys}-${mmdd}`] = name;
    }
  }
}

_build();

/**
 * 해당 날짜의 공휴일 이름을 반환합니다.
 * @param dateStr 'YYYY-MM-DD' 형식
 * @returns 공휴일 이름 또는 undefined
 */
export function getHolidayName(dateStr: string): string | undefined {
  return _cache[dateStr];
}

/**
 * 해당 날짜가 공휴일인지 여부를 반환합니다.
 */
export function isHoliday(dateStr: string): boolean {
  return dateStr in _cache;
}

/**
 * 모든 공휴일 맵을 반환합니다.
 * key: 'YYYY-MM-DD', value: 공휴일 이름
 */
export function getAllHolidays(): Record<string, string> {
  return _cache;
}
